
public class SwitchStatement {
    public static void main(String args[]) {
        int number = 2;
        switch (number) {
            case 1:
                System.out.println("samosa");
                break;
            case 2:
                System.out.println("chips");
                break;
            case 3:
                System.out.println("mango");
                break;
            default:
                System.out.println("wake up");

        }
    }
}
